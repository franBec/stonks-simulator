package dev.pollito.stonks_java.chaos.adapter.out;

import static dev.pollito.stonks_java.chaos.domain.ChaosEventSeverity.HIGH;
import static dev.pollito.stonks_java.chaos.domain.ChaosEventSeverity.MEDIUM;
import static dev.pollito.stonks_java.chaos.domain.ChaosEventType.DUMP;
import static dev.pollito.stonks_java.chaos.domain.ChaosEventType.HYPE_WAVE;
import static java.math.BigDecimal.valueOf;
import static java.time.OffsetDateTime.now;
import static java.util.Collections.emptyList;
import static java.util.List.of;
import static org.assertj.core.api.Assertions.assertThat;
import static org.mockito.ArgumentMatchers.any;
import static org.mockito.ArgumentMatchers.eq;
import static org.mockito.Mockito.when;

import dev.pollito.stonks_java.chaos.domain.ChaosEvent;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.InjectMocks;
import org.mockito.Mock;
import org.mockito.junit.jupiter.MockitoExtension;

@ExtendWith(MockitoExtension.class)
class ChaosEventGeneratorCompositeAdapterTest {

  @Mock private ChaosEventGeneratorOpenRouterAdapter openRouter;
  @Mock private ChaosEventGeneratorFallbackAdapter fallback;
  @InjectMocks private ChaosEventGeneratorCompositeAdapter adapter;

  @Test
  void returnsOpenRouterResultWhenItSucceeds() {
    var expected =
        new ChaosEvent(
            "AI Event",
            "GMEE",
            valueOf(20),
            "Generated by AI",
            of("GMEE"),
            "Source",
            now(),
            HYPE_WAVE,
            HIGH);
    when(openRouter.generate(emptyList(), emptyList(), null, null, null)).thenReturn(expected);

    assertThat(adapter.generate(emptyList(), emptyList(), null, null, null)).isEqualTo(expected);
  }

  @Test
  void fallsBackOnOpenRouterFailure() {
    when(openRouter.generate(eq(emptyList()), eq(emptyList()), any(), any(), any()))
        .thenThrow(new RuntimeException("AI service unavailable"));
    var fallbackEvent =
        new ChaosEvent(
            "Fallback Event",
            "GMEE",
            valueOf(15),
            "From catalog",
            of("GMEE"),
            "Source",
            now(),
            DUMP,
            MEDIUM);
    when(fallback.generate(eq(emptyList()), eq(emptyList()), any(), any(), any()))
        .thenReturn(fallbackEvent);

    assertThat(adapter.generate(emptyList(), emptyList(), null, null, null))
        .isEqualTo(fallbackEvent);
  }
}
